exports.postDeleteProduct = (req, res, next) => {
  const prodId = req.body.productId; //here we are requesting the productId 
  Product.findByPk(prodId) //once we got an Id 
    .then(product => {
      return product.destroy();//will delete the product using Destroy Function
    })
    .then(result => {
      console.log('DESTROYED PRODUCT');  
      res.redirect('/admin/products'); //once the product delete it will redirect to the Admin product page
    })
    .catch(err => console.log(err));
};
